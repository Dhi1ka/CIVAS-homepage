function openSidebar() {
  document.getElementById("leftSidebar").style.width = "50%";
}

function closeSidebar() {
  document.getElementById("leftSidebar").style.width = "0";
}

function backSidebar() {
  document.getElementById("leftSidebar").style.width = "0";
}

